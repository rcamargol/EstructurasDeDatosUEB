package co.edu.unbosque.model;

public class Nodo {
	//comentario para nodo
	private int info;
	private Nodo siguiente;
	 
	public int getInfo() {
		return info;
	}
	
	public void setInfo(int info) {
		this.info = info;
	}
	
	public Nodo getSiguiente() {
		return siguiente;
	}
	
	public void setSiguiente(Nodo siguiente) {
		this.siguiente = siguiente;
	}
	
}
